class Box:
    def __init__(self, x:float, y: float, width: float, height: float):
        self.x = x
        self.y = y
        self.width = width
        self.height = height

        def create_box(self):
            box = Box(
                x=self.x,
                y=self.y,
                width=self.width,
                height=self.height
            )
            return box


